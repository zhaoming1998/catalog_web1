// ========== SERIES TOGGLES ==========
function initToggles() {
    const toggles = document.querySelectorAll('.series-toggle');
    toggles.forEach(btn => {
        const cloned = btn.cloneNode(true);
        btn.parentNode.replaceChild(cloned, btn);
        cloned.addEventListener('click', () => {
            const target = cloned.dataset.target;
            const wrapper = document.querySelector(`[data-grid="${target}"]`);
            if (!wrapper) return;
            const textEl = cloned.querySelector('.series-toggle-text');
            const isOpen = cloned.classList.contains('open');
            if (isOpen) {
                cloned.classList.remove('open');
                wrapper.classList.remove('open');
                textEl.textContent = I18N[currentLang].toggle_collapse.replace('Collapse', '收起').replace('Expand', '展开') || I18N[currentLang].toggle_expand;
                if (!isOpen) textEl.textContent = I18N[currentLang].toggle_expand;
            } else {
                cloned.classList.add('open');
                wrapper.classList.add('open');
                textEl.textContent = I18N[currentLang].toggle_collapse;
                loadImagesInWrapper(wrapper);
            }
        });
    });
}

function loadImagesInWrapper(wrapper) {
    const imgs = wrapper.querySelectorAll('img[data-src]');
    imgs.forEach(img => {
        if (!img.src || img.src === '' || img.src === window.location.href) {
            img.src = img.dataset.src;
        }
        img.removeAttribute('data-src');
    });
}

